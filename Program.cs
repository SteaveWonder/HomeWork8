Console.Write("input massive size: ");
int size = Convert.ToInt32(Console.ReadLine());

int[,] nums = new int[size, size];

int num = 1;
int i = 0;
int j = 0;

while (num <= size * size)
{
    nums[i, j] = num;
    if (i <= j + 1 && i + j < size - 1)
        ++j;
    else 
    if (i < j && i + j >= size - 1)
        ++i;
    else 
    if (i >= j && i + j > size - 1)
        --j;
    else
        --i;
    ++num;
}
